# Entrada de dados
a = int(input("Insira um valor inteiro para A: "))
b = int(input("Insira um valor inteiro para B: "))
c = int(input("Insira um valor inteiro para C: "))
d = int(input("Insira um valor inteiro para D: "))

produto = a * c
soma = b + d

# Saída de dados
print(f"O produto de A e C é: {produto}")
print(f"A soma de B e D é: {soma}")